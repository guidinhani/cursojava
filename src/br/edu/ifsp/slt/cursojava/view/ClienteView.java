    private void limparFormulario(JTextField... campos) {
        for (JTextField campo : campos) {
            campo.setText("");
        }
    }

    private void validarPreenchimentoObrigatorio(JTextField... campos) throws PreenchimentoObrigatorioException {
        for (JTextField campo : campos) {
            if (campo.getText() == null || campo.getText().isEmpty()) {
                campo.setBackground(Color.YELLOW);
                throw new PreenchimentoObrigatorioException("Preenchimento obrigatÃ³rio: campo em amarelo nÃ£o pode estar em branco!");
            } else {
                campo.setBackground(Color.WHITE);
            }
        }
    }

    public static boolean isNumero(String string) {
        try {
            Integer.parseInt(string);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (this.cliente instanceof ClienteFisico) {
                validarPreenchimentoObrigatorio(txtNome, txtSobrenome, txtCidade);
                if (txtCodigoCliente.getText() != null && txtCodigoCliente.getText().length() != 0) {
                    if (!isNumero(txtCodigoCliente.getText())) {
                        JOptionPane.showMessageDialog(this, "Campo cÃ³digo precisa ser nÃºmerico!");
                        return;
                    }
                    SalvarClienteFisicoController salvarClienteFisico = new SalvarClienteFisicoController(clienteBusiness);
                    String mensagemSucesso = salvarClienteFisico.executar(txtCodigoCliente.getText(), txtNome.getText(), txtSobrenome.getText(),
                            txtLogradouro.getText(), txtCidade.getText(), (UnidadeFederativa) cmbEstado.getSelectedItem(), txtTelefone.getText(),
                            txtEmail.getText(), EstadoCliente.ATIVO, txtDocumento.getText());
                    JOptionPane.showMessageDialog(null, mensagemSucesso);
                } else {
                    return;
                }
                this.limparFormulario(txtCodigoCliente, txtNome, txtSobrenome, txtLogradouro, txtCidade,
                        txtTelefone, txtEmail, txtDocumento);
            }

            if (this.cliente instanceof ClienteJuridico) {
                validarPreenchimentoObrigatorio(txtNome, txtCidade, txtCidade);
                if (txtCodigoCliente.getText() != null && txtCodigoCliente.getText().length() != 0) {
                    if (!isNumero(txtCodigoCliente.getText())) {
                        JOptionPane.showMessageDialog(this, "Campo cÃ³digo precisa ser nÃºmerico!");
                        return;
                    }
                    SalvarClienteJuridicoController salvarClienteJuridico = new SalvarClienteJuridicoController(clienteBusiness);
                    String mensagemSucesso = salvarClienteJuridico.executar(txtCodigoCliente.getText(), txtNome.getText(), txtSobrenome.getText(),
                            txtLogradouro.getText(), txtCidade.getText(), (UnidadeFederativa) cmbEstado.getSelectedItem(), txtTelefone.getText(),
                            txtEmail.getText(), EstadoCliente.ATIVO, txtDocumento.getText());
                    JOptionPane.showMessageDialog(null, mensagemSucesso);
                } else {
                    return;
                }
                this.limparFormulario(txtCodigoCliente, txtNome, txtSobrenome, txtLogradouro, txtCidade,
                        txtTelefone, txtEmail, txtDocumento);
            }
        } catch (PreenchimentoObrigatorioException ex) {
            JOptionPane.showMessageDialog(null, ex.getMensagem());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        if (txtCodigoCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite o cÃ³digo do cliente!");
            return;
        }

        if (!isNumero(txtCodigoCliente.getText())) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um nÃºmero no cÃ³digo do cliente!");
            return;
        }

        VisualizarClienteController visualizarClienteController = new VisualizarClienteController(clienteBusiness);
        this.cliente = visualizarClienteController.executar(Integer.parseInt(txtCodigoCliente.getText()));
        if (this.cliente == null) {
            JOptionPane.showMessageDialog(this, "Cliente nÃ£o encontrado!");
        } else {
            JOptionPane.showMessageDialog(this, this.cliente);
        }
        this.txtCodigoCliente.setText(Integer.toString(this.cliente.getCodigo()));
        this.txtEstadoCliente.setText(this.cliente.getEstadoCliente().toString());
        this.txtNome.setText(this.cliente.getNome());
        this.txtSobrenome.setText(this.cliente.getSobrenome());
        this.txtLogradouro.setText(this.cliente.getEndereco().getLogradouro());
        this.txtCidade.setText(this.cliente.getEndereco().getLogradouro());
        this.cmbEstado.setSelectedItem(this.cliente.getEndereco().getUF());
        this.txtTelefone.setText(this.cliente.getTelefone());
        this.txtEmail.setText(this.cliente.getEmail());
        this.txtDocumento.setText(this.cliente.getDocumento());
    }//GEN-LAST:event_btnConsultarActionPerformed

